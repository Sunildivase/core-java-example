package Abstraction;

public class AbstractionVsInterface {

//    Abstraction:-
         //    - possibility of any method implementation
         //    - can have abstract and non-abstract methods
         //    - can have static or non-static variables
         //    - can have ny access modifiers.
         //    - can have construction.
         //    - can have single inheritance

//    Interface:-
        //     - no any method implementation
        //     - only abstract methods
        //     - can only have public static final constants
        //     - only have public access modifiers.
        //     - can not have constructions.
        //     - can have multiple inheritance
}

